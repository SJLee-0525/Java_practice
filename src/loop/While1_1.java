package loop;

public class While1_1 {
    // 1을 한 번씩 더해서 총 3번 더하는 코드 만들기
    public static void main(String[] args) {
        int a = 1;
        System.out.println("현재 숫자는 " + a);
        a++;
        System.out.println("현재 숫자는 " + a);
        a++;
        System.out.println("현재 숫자는 " + a);
    }
}
